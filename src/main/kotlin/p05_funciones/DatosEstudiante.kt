package org.example.p05_funciones

// Se quiere mostrar los datos de un estudiante, cada dato se mostrará
// en una función diferente
// nombre: mostrarNombre()
// carrera: visualizarCarrera()
// promedio: desplegarPromedio()

fun mostrarNombre() {
    println("Mi nombre es Diana Yaneth Ramirez Flores")
}

fun visualizarCarrera() {
    println("carrera: Ingenieria Informatica")
}

fun desplegarPromedio() {
    println("Promedio: 100")
}

fun main() {
    mostrarNombre()
    visualizarCarrera()
    desplegarPromedio()
}