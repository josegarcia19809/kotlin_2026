package org.example.p05_funciones

fun sumar(numero1: Double, numero2: Double) {
    val resultado = numero1 + numero2
    println("La suma es $resultado")
}


// Restar 2 enteros
// Multiplicar 2 Flotantes
// Dividir 2 dobles

fun main() {
    sumar(1.0, 5.0) // 6
    sumar(100.0, 15.0) // 115
    sumar(40.0, 20.0) // 60
}